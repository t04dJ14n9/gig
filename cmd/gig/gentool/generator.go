// Package gentool generates registration code so the v2 SSA
// interpreter can call into a Go package's exported API.
//
// For each requested package (e.g. "fmt"), PackageImport produces a
// file that, at init time, registers the package's functions, vars,
// constants, and named types with the importer's global registry.
// The v2 host bridge in package host reads these registrations and
// dispatches calls through generated direct wrappers when possible,
// falling back to reflect.Call only for signatures that cannot be
// expressed from generated Go code.
//
// # Generated shape
//
//	// Code generated by gig/gentool. DO NOT EDIT.
//	package packages
//
//	import (
//	    "fmt"
//	    "github.com/t04dJ14n9/gig/importer"
//	    "github.com/t04dJ14n9/gig/value"
//	)
//
//	func init() {
//	    pkg := importer.RegisterPackage("fmt", "fmt")
//	    pkg.AddFunction("Sprintf", fmt.Sprintf, "", directCallSprintf)
//	    // ...
//	}
package gentool

import (
	"fmt"
	"go/ast"
	"go/format"
	"go/importer"
	"go/parser"
	"go/token"
	"go/types"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

const valueImportPath = "github.com/t04dJ14n9/gig/value"

// ParsePkgsFile parses a Go source file and extracts all import paths.
// The file should contain blank imports (_ "package/path") for packages
// to register.
func ParsePkgsFile(filePath string) ([]string, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, filePath, nil, parser.ImportsOnly)
	if err != nil {
		return nil, fmt.Errorf("parse %s: %w", filePath, err)
	}
	var paths []string
	for _, imp := range f.Imports {
		p := strings.Trim(imp.Path.Value, `"`)
		if p == "" {
			continue
		}
		paths = append(paths, p)
	}
	if len(paths) == 0 {
		return nil, fmt.Errorf("no imports found in %s", filePath)
	}
	return paths, nil
}

// GenerateBarrelImport creates a barrel file that blank-imports all
// generated packages so their init() registrations fire when the
// `packages` directory is imported once at the top of the host binary.
func GenerateBarrelImport(outDir string, modulePath string, generatedPkgs []string) error {
	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	b.WriteString("// Blank-imports every generated package so their init() registrations fire.\n")
	b.WriteString("package packages\n\n")
	b.WriteString("import (\n")
	for _, pkgName := range generatedPkgs {
		fmt.Fprintf(b, "\t_ %q\n", modulePath+"/packages/"+pkgName)
	}
	b.WriteString(")\n")
	code, err := format.Source([]byte(b.String()))
	if err != nil {
		code = []byte(b.String())
	}
	outFile := filepath.Join(outDir, "packages", "stdlib.go")
	return os.WriteFile(outFile, code, 0o666)
}

// PackageImport generates registration code for a single host package.
//
// outDir is the directory for generated files; pkgName is the Go
// package name for the output (typically "packages").
func PackageImport(path string, outDir string, pkgName string) error {
	pkg, err := importer.ForCompiler(token.NewFileSet(), "source", nil).Import(path)
	if err != nil {
		return err
	}

	importAlias := ""
	if strings.Contains(path, "/") {
		importAlias = sanitizePkgName(path)
	}
	pkgRef := pkg.Name()
	if importAlias != "" {
		pkgRef = importAlias
	}

	var funcs []*funcInfo
	var consts []*constInfo
	var vars []*varInfo
	var typeNames []*typeInfo

	scope := pkg.Scope()
	for _, name := range scope.Names() {
		if !ast.IsExported(name) {
			continue
		}
		obj := scope.Lookup(name)
		switch o := obj.(type) {
		case *types.Func:
			sig := o.Type().(*types.Signature)
			if sig.TypeParams().Len() > 0 {
				continue
			}
			funcs = append(funcs, &funcInfo{Name: name, Sig: sig})
		case *types.Const:
			consts = append(consts, &constInfo{Name: name, Obj: o})
		case *types.Var:
			vars = append(vars, &varInfo{Name: name, Obj: o})
		case *types.TypeName:
			if o.IsAlias() {
				continue
			}
			if named, ok := o.Type().(*types.Named); ok {
				if named.TypeParams().Len() > 0 {
					continue
				}
			}
			if iface, ok := o.Type().Underlying().(*types.Interface); ok {
				if !iface.IsMethodSet() {
					continue
				}
			}
			typeNames = append(typeNames, &typeInfo{Name: name, Obj: o})
		}
	}

	if len(funcs) == 0 && len(consts) == 0 && len(vars) == 0 && len(typeNames) == 0 {
		fmt.Printf("  (skipped, nothing to register)\n")
		return nil
	}

	goPkgName := sanitizePkgName(path)
	directPrefix := exportedIdent(goPkgName)
	needReflect := false
	for _, ti := range typeNames {
		if typeToReflectExpr(ti.Obj.Type(), pkgRef) != "" {
			needReflect = true
			break
		}
	}
	typeImports := map[string]string{}
	for _, fi := range funcs {
		if params, variadicElem, ok := directSignature(fi.Sig, path, pkgRef, typeImports); ok {
			fi.Direct = true
			fi.ParamTypes = params
			fi.VariadicElem = variadicElem
		}
	}
	needDirect := false
	for _, fi := range funcs {
		if fi.Direct {
			needDirect = true
			break
		}
	}

	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	fmt.Fprintf(b, "package %s\n\n", pkgName)

	b.WriteString("import (\n")
	imports := newImportSet()
	if importAlias != "" {
		imports.add(path, importAlias)
	} else {
		imports.add(path, "")
	}
	typeImportPaths := make([]string, 0, len(typeImports))
	for path := range typeImports {
		typeImportPaths = append(typeImportPaths, path)
	}
	sort.Strings(typeImportPaths)
	for _, path := range typeImportPaths {
		imports.add(path, typeImports[path])
	}
	if needDirect {
		imports.add("fmt", "")
		imports.add("reflect", "")
		imports.add(valueImportPath, "")
	} else if needReflect {
		imports.add("reflect", "")
	}
	imports.add("github.com/t04dJ14n9/gig/importer", "")
	for _, imp := range imports.list {
		if imp.alias != "" {
			fmt.Fprintf(b, "\t%s %q\n", imp.alias, imp.path)
		} else {
			fmt.Fprintf(b, "\t%q\n", imp.path)
		}
	}
	b.WriteString(")\n\n")

	b.WriteString("func init() {\n")
	fmt.Fprintf(b, "\tpkg := importer.RegisterPackage(%q, %q)\n\n", path, pkg.Name())

	if len(funcs) > 0 {
		b.WriteString("\t// Functions\n")
		for _, fi := range funcs {
			if fi.Direct {
				fmt.Fprintf(b, "\tpkg.AddFunction(%q, %s.%s, \"\", %s)\n", fi.Name, pkgRef, fi.Name, directWrapperName(directPrefix, fi.Name))
			} else {
				fmt.Fprintf(b, "\tpkg.AddFunction(%q, %s.%s, \"\")\n", fi.Name, pkgRef, fi.Name)
			}
		}
		b.WriteString("\n")
	}

	if len(consts) > 0 {
		b.WriteString("\t// Constants\n")
		for _, ci := range consts {
			constRef := fmt.Sprintf("%s.%s", pkgRef, ci.Name)
			if needsUintCast(ci.Obj) {
				constRef = fmt.Sprintf("uint64(%s)", constRef)
			}
			fmt.Fprintf(b, "\tpkg.AddConstant(%q, %s, \"\")\n", ci.Name, constRef)
		}
		b.WriteString("\n")
	}

	if len(vars) > 0 {
		b.WriteString("\t// Variables\n")
		for _, vi := range vars {
			fmt.Fprintf(b, "\tpkg.AddVariable(%q, &%s.%s, \"\")\n", vi.Name, pkgRef, vi.Name)
		}
		b.WriteString("\n")
	}

	if len(typeNames) > 0 {
		b.WriteString("\t// Types\n")
		for _, ti := range typeNames {
			expr := typeToReflectExpr(ti.Obj.Type(), pkgRef)
			if expr != "" {
				fmt.Fprintf(b, "\tpkg.AddType(%q, %s, \"\")\n", ti.Name, expr)
			}
		}
		b.WriteString("\n")
	}

	b.WriteString("}\n")
	if needDirect {
		writeDirectHelpers(b, directPrefix)
		for _, fi := range funcs {
			if fi.Direct {
				writeDirectWrapper(b, pkgRef, fi, directPrefix)
			}
		}
	}

	src := b.String()
	code, err := format.Source([]byte(src))
	if err != nil {
		code = []byte(src)
		fmt.Printf("  format error for %s: %s\n", path, err.Error())
	}

	filename := filepath.Join(outDir, goPkgName+".go")
	fmt.Printf("  -> %s\n", filename)
	return os.WriteFile(filename, code, 0o666)
}

type importSpec struct {
	path  string
	alias string
}

type importSet struct {
	seen map[string]int
	list []importSpec
}

func newImportSet() *importSet {
	return &importSet{seen: map[string]int{}}
}

func (s *importSet) add(path, alias string) {
	if path == "" {
		return
	}
	if idx, ok := s.seen[path]; ok {
		if s.list[idx].alias == "" && alias != "" {
			s.list[idx].alias = alias
		}
		return
	}
	s.seen[path] = len(s.list)
	s.list = append(s.list, importSpec{path: path, alias: alias})
}

func directSignature(sig *types.Signature, targetPath, targetRef string, imports map[string]string) ([]string, string, bool) {
	params := sig.Params()
	paramTypes := make([]string, params.Len())
	qualifier := func(pkg *types.Package) string {
		if pkg == nil {
			return ""
		}
		if pkg.Path() == targetPath {
			return targetRef
		}
		name := importNameFor(pkg)
		if name == pkg.Name() {
			imports[pkg.Path()] = ""
		} else {
			imports[pkg.Path()] = name
		}
		return name
	}
	for i := 0; i < params.Len(); i++ {
		t := params.At(i).Type()
		if !canNameType(t, targetPath) {
			return nil, "", false
		}
		if typeUsesUnsafePointer(t) {
			imports["unsafe"] = ""
		}
		paramTypes[i] = types.TypeString(t, qualifier)
	}
	var variadicElem string
	if sig.Variadic() && params.Len() > 0 {
		last := params.At(params.Len() - 1).Type()
		slice, ok := last.(*types.Slice)
		if !ok {
			return nil, "", false
		}
		elem := slice.Elem()
		if !canNameType(elem, targetPath) {
			return nil, "", false
		}
		if typeUsesUnsafePointer(elem) {
			imports["unsafe"] = ""
		}
		variadicElem = types.TypeString(elem, qualifier)
	}
	return paramTypes, variadicElem, true
}

func importNameFor(pkg *types.Package) string {
	if pkg == nil {
		return ""
	}
	if !strings.Contains(pkg.Path(), "/") && !strings.Contains(pkg.Path(), ".") && !strings.Contains(pkg.Path(), "-") {
		return pkg.Name()
	}
	return sanitizePkgName(pkg.Path())
}

func canNameType(t types.Type, targetPath string) bool {
	switch t := t.(type) {
	case *types.Basic:
		return true
	case *types.Array:
		return canNameType(t.Elem(), targetPath)
	case *types.Slice:
		return canNameType(t.Elem(), targetPath)
	case *types.Pointer:
		return canNameType(t.Elem(), targetPath)
	case *types.Map:
		return canNameType(t.Key(), targetPath) && canNameType(t.Elem(), targetPath)
	case *types.Chan:
		return canNameType(t.Elem(), targetPath)
	case *types.Named:
		obj := t.Obj()
		if obj != nil && obj.Pkg() != nil && !ast.IsExported(obj.Name()) {
			return false
		}
		for i := 0; i < t.TypeArgs().Len(); i++ {
			if !canNameType(t.TypeArgs().At(i), targetPath) {
				return false
			}
		}
		return true
	case *types.Alias:
		obj := t.Obj()
		if obj != nil && obj.Pkg() != nil && !ast.IsExported(obj.Name()) {
			return false
		}
		for i := 0; i < t.TypeArgs().Len(); i++ {
			if !canNameType(t.TypeArgs().At(i), targetPath) {
				return false
			}
		}
		return true
	case *types.Signature:
		if t.TypeParams().Len() > 0 {
			return false
		}
		return canNameTuple(t.Params(), targetPath) && canNameTuple(t.Results(), targetPath)
	case *types.Tuple:
		return canNameTuple(t, targetPath)
	case *types.Struct:
		for i := 0; i < t.NumFields(); i++ {
			if !canNameType(t.Field(i).Type(), targetPath) {
				return false
			}
		}
		return true
	case *types.Interface:
		for i := 0; i < t.NumEmbeddeds(); i++ {
			if !canNameType(t.EmbeddedType(i), targetPath) {
				return false
			}
		}
		for i := 0; i < t.NumExplicitMethods(); i++ {
			if !canNameType(t.ExplicitMethod(i).Type(), targetPath) {
				return false
			}
		}
		return true
	case *types.TypeParam:
		return false
	default:
		return false
	}
}

func canNameTuple(tuple *types.Tuple, targetPath string) bool {
	if tuple == nil {
		return true
	}
	for i := 0; i < tuple.Len(); i++ {
		if !canNameType(tuple.At(i).Type(), targetPath) {
			return false
		}
	}
	return true
}

func typeUsesUnsafePointer(t types.Type) bool {
	switch t := t.(type) {
	case *types.Basic:
		return t.Kind() == types.UnsafePointer
	case *types.Array:
		return typeUsesUnsafePointer(t.Elem())
	case *types.Slice:
		return typeUsesUnsafePointer(t.Elem())
	case *types.Pointer:
		return typeUsesUnsafePointer(t.Elem())
	case *types.Map:
		return typeUsesUnsafePointer(t.Key()) || typeUsesUnsafePointer(t.Elem())
	case *types.Chan:
		return typeUsesUnsafePointer(t.Elem())
	case *types.Named:
		for i := 0; i < t.TypeArgs().Len(); i++ {
			if typeUsesUnsafePointer(t.TypeArgs().At(i)) {
				return true
			}
		}
		return false
	case *types.Alias:
		for i := 0; i < t.TypeArgs().Len(); i++ {
			if typeUsesUnsafePointer(t.TypeArgs().At(i)) {
				return true
			}
		}
		return false
	case *types.Signature:
		return tupleUsesUnsafePointer(t.Params()) || tupleUsesUnsafePointer(t.Results())
	case *types.Tuple:
		return tupleUsesUnsafePointer(t)
	case *types.Struct:
		for i := 0; i < t.NumFields(); i++ {
			if typeUsesUnsafePointer(t.Field(i).Type()) {
				return true
			}
		}
		return false
	case *types.Interface:
		for i := 0; i < t.NumEmbeddeds(); i++ {
			if typeUsesUnsafePointer(t.EmbeddedType(i)) {
				return true
			}
		}
		for i := 0; i < t.NumExplicitMethods(); i++ {
			if typeUsesUnsafePointer(t.ExplicitMethod(i).Type()) {
				return true
			}
		}
		return false
	default:
		return false
	}
}

func tupleUsesUnsafePointer(tuple *types.Tuple) bool {
	if tuple == nil {
		return false
	}
	for i := 0; i < tuple.Len(); i++ {
		if typeUsesUnsafePointer(tuple.At(i).Type()) {
			return true
		}
	}
	return false
}

func exportedIdent(s string) string {
	parts := strings.FieldsFunc(s, func(r rune) bool {
		return r == '_' || r == '-' || r == '.' || r == '/'
	})
	if len(parts) == 0 {
		return "Pkg"
	}
	var b strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		b.WriteString(strings.ToUpper(part[:1]))
		if len(part) > 1 {
			b.WriteString(part[1:])
		}
	}
	if b.Len() == 0 {
		return "Pkg"
	}
	return b.String()
}

func directWrapperName(prefix, name string) string {
	return "directCall" + prefix + name
}

func writeDirectHelpers(b *strings.Builder, prefix string) {
	fmt.Fprintf(b, `
func directArg%s[T any](v value.Value) (T, error) {
	var zero T
	rt := reflect.TypeFor[T]()
	rv, err := value.DefaultConverter().ToReflect(v, rt)
	if err != nil {
		return zero, err
	}
	if !rv.IsValid() {
		return zero, nil
	}
	if rv.Type().AssignableTo(rt) {
		return rv.Interface().(T), nil
	}
	if rv.Type().ConvertibleTo(rt) {
		return rv.Convert(rt).Interface().(T), nil
	}
	return zero, fmt.Errorf("cannot convert %%s to %%s", rv.Type(), rt)
}

func directVariadicArgs%s[T any](args []value.Value) ([]T, error) {
	if len(args) == 1 {
		if packed, err := directArg%s[[]T](args[0]); err == nil {
			return packed, nil
		}
		if rv, ok := args[0].Reflect(); ok && rv.IsValid() {
			for rv.Kind() == reflect.Interface && !rv.IsNil() {
				rv = rv.Elem()
			}
			if rv.Kind() == reflect.Slice {
				out := make([]T, rv.Len())
				conv := value.DefaultConverter()
				for i := 0; i < rv.Len(); i++ {
					vv, err := conv.FromReflect(rv.Index(i))
					if err != nil {
						return nil, fmt.Errorf("variadic explode %%d: %%w", i, err)
					}
					out[i], err = directArg%s[T](vv)
					if err != nil {
						return nil, fmt.Errorf("variadic arg %%d: %%w", i, err)
					}
				}
				return out, nil
			}
		}
	}
	out := make([]T, len(args))
	for i, arg := range args {
		v, err := directArg%s[T](arg)
		if err != nil {
			return nil, fmt.Errorf("variadic arg %%d: %%w", i, err)
		}
		out[i] = v
	}
	return out, nil
}

func directResults%s(vals ...any) ([]value.Value, error) {
	out := make([]value.Value, len(vals))
	conv := value.DefaultConverter()
	for i, v := range vals {
		vv, err := conv.FromAny(v)
		if err != nil {
			return nil, fmt.Errorf("result %%d: %%w", i, err)
		}
		out[i] = vv
	}
	return out, nil
}
`, prefix, prefix, prefix, prefix, prefix, prefix)
}

func writeDirectWrapper(b *strings.Builder, pkgRef string, fi *funcInfo, prefix string) {
	fmt.Fprintf(b, "\nfunc %s(args []value.Value) ([]value.Value, error) {\n", directWrapperName(prefix, fi.Name))
	params := fi.Sig.Params()
	fixed := params.Len()
	if fi.Sig.Variadic() {
		fixed--
		fmt.Fprintf(b, "\tif len(args) < %d {\n", fixed)
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"arg count %%d < %d\", len(args))\n", fixed)
		b.WriteString("\t}\n")
	} else {
		fmt.Fprintf(b, "\tif len(args) != %d {\n", params.Len())
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"arg count %%d != %d\", len(args))\n", params.Len())
		b.WriteString("\t}\n")
	}
	for i := 0; i < fixed; i++ {
		fmt.Fprintf(b, "\ta%d, err := directArg%s[%s](args[%d])\n", i, prefix, fi.ParamTypes[i], i)
		b.WriteString("\tif err != nil {\n")
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"arg %d: %%w\", err)\n", i)
		b.WriteString("\t}\n")
	}
	if fi.Sig.Variadic() {
		fmt.Fprintf(b, "\ta%d, err := directVariadicArgs%s[%s](args[%d:])\n", fixed, prefix, fi.VariadicElem, fixed)
		b.WriteString("\tif err != nil {\n")
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"arg %d: %%w\", err)\n", fixed)
		b.WriteString("\t}\n")
	}
	writeDirectFunctionCall(b, pkgRef, fi, fixed, prefix)
	b.WriteString("}\n")
}

func writeDirectFunctionCall(b *strings.Builder, pkgRef string, fi *funcInfo, fixed int, prefix string) {
	resultCount := fi.Sig.Results().Len()
	if resultCount > 0 {
		for i := 0; i < resultCount; i++ {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(b, "r%d", i)
		}
		fmt.Fprintf(b, " := %s.%s(", pkgRef, fi.Name)
	} else {
		fmt.Fprintf(b, "\t%s.%s(", pkgRef, fi.Name)
	}
	for i := 0; i < fi.Sig.Params().Len(); i++ {
		if i > 0 {
			b.WriteString(", ")
		}
		fmt.Fprintf(b, "a%d", i)
		if fi.Sig.Variadic() && i == fixed {
			b.WriteString("...")
		}
	}
	b.WriteString(")\n")
	if resultCount == 0 {
		b.WriteString("\treturn nil, nil\n")
		return
	}
	fmt.Fprintf(b, "\treturn directResults%s(", prefix)
	for i := 0; i < resultCount; i++ {
		if i > 0 {
			b.WriteString(", ")
		}
		fmt.Fprintf(b, "r%d", i)
	}
	b.WriteString(")\n")
}

// --- Type info structs ---

type funcInfo struct {
	Name         string
	Sig          *types.Signature
	Direct       bool
	ParamTypes   []string
	VariadicElem string
}

type constInfo struct {
	Name string
	Obj  *types.Const
}

type varInfo struct {
	Name string
	Obj  *types.Var
}

type typeInfo struct {
	Name string
	Obj  *types.TypeName
}
