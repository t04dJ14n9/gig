// Package gentool provides the core dependency generation logic for gig.
//
// Gentool generates Go source files that register external packages for the interpreter.
// For each imported package, it creates wrapper code that:
//   - Registers the package with the importer
//   - Adds functions with optional DirectCall wrappers for fast dispatch
//   - Adds variables, constants, and types
//
// # Generated Code Structure
//
// For each package (e.g., "fmt"), gentool generates a file like:
//
//	package packages
//
//	import (
//	    "fmt"
//	    "github.com/t04dJ14n9/gig/importer"
//	    "github.com/t04dJ14n9/gig/value"
//	)
//
//	func init() {
//	    pkg := importer.RegisterPackage("fmt", "fmt")
//	    pkg.AddFunction("Sprintf", fmt.Sprintf, "", direct_fmt_Sprintf)
//	    // ... more functions
//	}
//
//	func direct_fmt_Sprintf(args []value.Value) value.Value {
//	    format := args[0].String()
//	    // ... convert arguments and call fmt.Sprintf
//	    return value.FromInterface(result)
//	}
//
// # DirectCall Wrappers
//
// DirectCall wrappers provide fast function dispatch by:
//  1. Extracting typed values from value.Value arguments
//  2. Calling the native Go function directly
//  3. Wrapping the result in a value.Value
//
// This avoids the overhead of reflect.Call for common cases.
// DirectCall wrappers are only generated for functions with supported parameter types
// (basic types, slices of basic types, and empty interfaces).
package gentool

import (
	"fmt"
	"go/ast"
	"go/format"
	"go/importer"
	"go/parser"
	"go/token"
	"go/types"
	"os"
	"path/filepath"
	"strings"
)

// ParsePkgsFile parses a Go source file and extracts all import paths.
// The file should contain blank imports (_ "package/path") for packages to register.
// Returns a list of import paths or an error if parsing fails.
func ParsePkgsFile(filePath string) ([]string, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, filePath, nil, parser.ImportsOnly)
	if err != nil {
		return nil, fmt.Errorf("parse %s: %w", filePath, err)
	}

	var paths []string
	for _, imp := range f.Imports {
		p := strings.Trim(imp.Path.Value, `"`)
		if p == "" {
			continue
		}
		paths = append(paths, p)
	}
	if len(paths) == 0 {
		return nil, fmt.Errorf("no imports found in %s", filePath)
	}
	return paths, nil
}

// GenerateBarrelImport creates a barrel file that blank-imports all generated packages.
// This ensures all init() functions are called when the packages package is imported.
func GenerateBarrelImport(outDir string, modulePath string, generatedPkgs []string) error {
	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	b.WriteString("// This file blank-imports all generated packages to trigger their init() registrations.\n")
	b.WriteString("package packages\n\n")
	b.WriteString("import (\n")
	for _, pkgName := range generatedPkgs {
		b.WriteString(fmt.Sprintf("\t_ %q\n", modulePath+"/packages/"+pkgName))
	}
	b.WriteString(")\n")

	code, err := format.Source([]byte(b.String()))
	if err != nil {
		code = []byte(b.String())
	}

	outFile := filepath.Join(outDir, "packages", "stdlib.go")
	return os.WriteFile(outFile, code, 0o666)
}

// PackageImport generates registration code for a single package.
// It reads the package's exported symbols and generates:
//   - Package registration in an init() function
//   - Function, variable, constant, and type registrations
//   - DirectCall wrappers for eligible functions
//
// outDir is the output directory for generated files.
// pkgName is the Go package name for generated files (typically "packages").
func PackageImport(path string, outDir string, pkgName string) error {
	pkg, err := importer.ForCompiler(token.NewFileSet(), "source", nil).Import(path)
	if err != nil {
		return err
	}

	currentPkgPath = path

	// Determine import alias for sub-packages.
	importAlias := ""
	if strings.Contains(path, "/") {
		importAlias = sanitizePkgName(path)
	}
	pkgRef := pkg.Name()
	if importAlias != "" {
		pkgRef = importAlias
	}

	var funcs []*funcInfo
	var consts []*constInfo
	var vars []*varInfo
	var typeNames []*typeInfo

	scope := pkg.Scope()
	for _, name := range scope.Names() {
		if !ast.IsExported(name) {
			continue
		}
		obj := scope.Lookup(name)

		switch o := obj.(type) {
		case *types.Func:
			sig := o.Type().(*types.Signature)
			if sig.TypeParams().Len() > 0 {
				continue
			}
			fi := &funcInfo{Name: name, Sig: sig}
			fi.DirectCall = generateDirectCall(fi, pkgRef)
			funcs = append(funcs, fi)

		case *types.Const:
			consts = append(consts, &constInfo{Name: name, Obj: o})

		case *types.Var:
			vars = append(vars, &varInfo{Name: name, Obj: o})

		case *types.TypeName:
			if o.IsAlias() {
				continue
			}
			if named, ok := o.Type().(*types.Named); ok {
				if named.TypeParams().Len() > 0 {
					continue
				}
			}
			if iface, ok := o.Type().Underlying().(*types.Interface); ok {
				if !iface.IsMethodSet() {
					continue
				}
			}
			typeNames = append(typeNames, &typeInfo{Name: name, Obj: o})
		}
	}

	if len(funcs) == 0 && len(consts) == 0 && len(vars) == 0 && len(typeNames) == 0 {
		fmt.Printf("  (skipped, nothing to register)\n")
		return nil
	}

	goPkgName := sanitizePkgName(path)
	needReflect := false
	for _, ti := range typeNames {
		if typeToReflectExpr(ti.Obj.Type(), pkgRef) != "" {
			needReflect = true
			break
		}
	}
	hasDirectCalls := false
	// Collect cross-package imports needed by DirectCall wrappers
	crossPkgImports := make(map[string]string) // path -> alias
	for _, fi := range funcs {
		if fi.DirectCall != "" {
			hasDirectCalls = true
			collectCrossPkgImports(fi.Sig, path, crossPkgImports)
		}
	}

	// Generate method DirectCall wrappers for each type
	var allMethodDCs []*methodDirectCallInfo
	for _, ti := range typeNames {
		named, ok := ti.Obj.Type().(*types.Named)
		if !ok {
			continue
		}
		// Skip interfaces — methods are dispatched via the implementing type
		if _, isIface := named.Underlying().(*types.Interface); isIface {
			continue
		}
		methodDCs := generateMethodDirectCalls(named, pkgRef, ti.Name)
		if len(methodDCs) > 0 {
			hasDirectCalls = true
			allMethodDCs = append(allMethodDCs, methodDCs...)
			// Collect cross-package imports from method signatures
			for _, mdc := range methodDCs {
				collectMethodImports(named, mdc.MethodName, path, crossPkgImports)
			}
		}
	}

	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	b.WriteString(fmt.Sprintf("package %s\n\n", pkgName))

	b.WriteString("import (\n")
	if importAlias != "" {
		b.WriteString(fmt.Sprintf("\t%s %q\n", importAlias, path))
	} else {
		b.WriteString(fmt.Sprintf("\t%q\n", path))
	}
	if needReflect {
		b.WriteString("\t\"reflect\"\n")
	}
	// Add extra imports for packages that need fmt sanitization helpers
	sanitizeImports := map[string]string{}
	if fmtSanitizePackages[path] {
		sanitizeImports = fmtSanitizeExtraImports()
	}

	// Merge sanitize imports into cross-package imports
	for impPath, impAlias := range sanitizeImports {
		if _, exists := crossPkgImports[impPath]; !exists {
			crossPkgImports[impPath] = impAlias
		}
	}

	// Add cross-package imports needed by DirectCall wrappers
	for impPath, impAlias := range crossPkgImports {
		// Skip reflect if already imported above
		if impPath == "reflect" && needReflect {
			continue
		}
		// Skip the package's own import (already imported at top)
		if impPath == path {
			continue
		}
		if impAlias != "" {
			b.WriteString(fmt.Sprintf("\t%s %q\n", impAlias, impPath))
		} else {
			b.WriteString(fmt.Sprintf("\t%q\n", impPath))
		}
	}
	b.WriteString("\n")
	b.WriteString("\t\"github.com/t04dJ14n9/gig/importer\"\n")
	if hasDirectCalls {
		b.WriteString("\t\"github.com/t04dJ14n9/gig/value\"\n")
	}
	b.WriteString(")\n\n")

	b.WriteString("func init() {\n")
	b.WriteString(fmt.Sprintf("\tpkg := importer.RegisterPackage(%q, %q)\n\n", path, pkg.Name()))

	if len(funcs) > 0 {
		b.WriteString("\t// Functions\n")
		for _, fi := range funcs {
			directRef := "nil"
			if fi.DirectCall != "" {
				directRef = fmt.Sprintf("direct_%s_%s", pkgRef, fi.Name)
			}
			b.WriteString(fmt.Sprintf("\tpkg.AddFunction(%q, %s.%s, \"\", %s)\n",
				fi.Name, pkgRef, fi.Name, directRef))
		}
		b.WriteString("\n")
	}

	if len(consts) > 0 {
		b.WriteString("\t// Constants\n")
		for _, ci := range consts {
			constRef := fmt.Sprintf("%s.%s", pkgRef, ci.Name)
			// Untyped int constants that are unsigned may overflow int.
			// Wrap with uint64() to prevent compile errors.
			if needsUintCast(ci.Obj) {
				constRef = fmt.Sprintf("uint64(%s)", constRef)
			}
			b.WriteString(fmt.Sprintf("\tpkg.AddConstant(%q, %s, \"\")\n", ci.Name, constRef))
		}
		b.WriteString("\n")
	}

	if len(vars) > 0 {
		b.WriteString("\t// Variables\n")
		for _, vi := range vars {
			b.WriteString(fmt.Sprintf("\tpkg.AddVariable(%q, &%s.%s, \"\")\n", vi.Name, pkgRef, vi.Name))
		}
		b.WriteString("\n")
	}

	if len(typeNames) > 0 {
		b.WriteString("\t// Types\n")
		for _, ti := range typeNames {
			expr := typeToReflectExpr(ti.Obj.Type(), pkgRef)
			if expr != "" {
				b.WriteString(fmt.Sprintf("\tpkg.AddType(%q, %s, \"\")\n", ti.Name, expr))
			}
		}
		b.WriteString("\n")
	}

	// Method DirectCall registrations
	if len(allMethodDCs) > 0 {
		b.WriteString("\t// Method DirectCalls\n")
		for _, mdc := range allMethodDCs {
			b.WriteString(fmt.Sprintf("\tpkg.AddMethodDirectCall(%q, %q, %s)\n",
				mdc.TypeName, mdc.MethodName, mdc.FuncName))
		}
		b.WriteString("\n")
	}

	b.WriteString("}\n\n")

	for _, fi := range funcs {
		if fi.DirectCall != "" {
			b.WriteString(fi.DirectCall)
			b.WriteString("\n")
		}
	}

	// Method DirectCall wrapper functions
	for _, mdc := range allMethodDCs {
		b.WriteString(mdc.Code)
		b.WriteString("\n")
	}

	// Emit fmt sanitization helper code for packages that need it
	if fmtSanitizePackages[path] {
		b.WriteString(fmtSanitizeHelperCode())
		b.WriteString("\n")
	}

	src := b.String()
	code, err := format.Source([]byte(src))
	if err != nil {
		code = []byte(src)
		fmt.Printf("  format error for %s: %s\n", path, err.Error())
	}

	// Output file: outDir/goPkgName.go (e.g., mydep/fmt.go)
	filename := filepath.Join(outDir, goPkgName+".go")

	fmt.Printf("  -> %s\n", filename)
	return os.WriteFile(filename, code, 0o666)
}

// --- Type info structs ---

type funcInfo struct {
	Name       string
	Sig        *types.Signature
	DirectCall string
}

type constInfo struct {
	Name string
	Obj  *types.Const
}

type varInfo struct {
	Name string
	Obj  *types.Var
}

type typeInfo struct {
	Name string
	Obj  *types.TypeName
}
