package gentool

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

func writeGeneratedPackage(outDir string, plan packageGenerationPlan) error {
	// Emission deliberately writes from a completed plan. This keeps generation
	// deterministic and avoids discovering new imports halfway through writing a
	// file, which used to make this path harder to review.
	src := emitPackageSource(plan)
	code := formatPackageSource(plan.Path, src)
	filename := filepath.Join(outDir, plan.Refs.GoPkgName+".go")

	fmt.Printf("  -> %s\n", filename)
	return os.WriteFile(filename, code, 0o666)
}

func formatPackageSource(path, src string) []byte {
	code, err := format.Source([]byte(src))
	if err != nil {
		fmt.Printf("  format error for %s: %s\n", path, err.Error())
		return []byte(src)
	}
	return code
}

func emitPackageSource(plan packageGenerationPlan) string {
	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	writeGeneratedf(b, "package %s\n\n", plan.PkgName)
	emitPackageImports(b, plan)
	emitPackageInit(b, plan)
	emitFunctionDirectCalls(b, plan.Symbols.Funcs)
	emitMethodDirectCalls(b, plan.Adapters.MethodDirectCalls)
	emitInterfaceProxies(b, plan.Adapters.InterfaceProxies)
	return b.String()
}

func emitPackageImports(b *strings.Builder, plan packageGenerationPlan) {
	// Import order here mirrors the old generator output: target package first,
	// optional helper imports, then gig runtime packages. go/format will sort
	// within groups but preserves the blank line between external and gig imports.
	refs := plan.Refs
	adapters := plan.Adapters
	b.WriteString("import (\n")
	if refs.ImportAlias != "" {
		writeGeneratedf(b, "\t%s %q\n", refs.ImportAlias, plan.Path)
	} else {
		writeGeneratedf(b, "\t%q\n", plan.Path)
	}
	if adapters.NeedReflect {
		b.WriteString("\t\"reflect\"\n")
	}
	for impPath, impAlias := range adapters.CrossPkgImports {
		if shouldSkipGeneratedImport(impPath, plan.Path, adapters.NeedReflect) {
			continue
		}
		if impAlias != "" {
			writeGeneratedf(b, "\t%s %q\n", impAlias, impPath)
		} else {
			writeGeneratedf(b, "\t%q\n", impPath)
		}
	}
	b.WriteString("\n")
	b.WriteString("\t\"github.com/t04dJ14n9/gig/importer\"\n")
	if adapters.HasDirectCalls || len(adapters.InterfaceProxies) > 0 {
		b.WriteString("\t\"github.com/t04dJ14n9/gig/model/value\"\n")
	}
	if len(adapters.InterfaceProxies) > 0 {
		b.WriteString("\t\"github.com/t04dJ14n9/gig/model/external\"\n")
	}
	b.WriteString(")\n\n")
}

func shouldSkipGeneratedImport(importPath, packagePath string, needReflect bool) bool {
	return (importPath == "reflect" && needReflect) || importPath == packagePath
}

func emitPackageInit(b *strings.Builder, plan packageGenerationPlan) {
	// Registration order is stable and matches the previous monolithic emitter:
	// functions, constants, variables, types, interface proxies, then method
	// direct calls. Generated tests rely on those sections being present.
	b.WriteString("func init() {\n")
	writeGeneratedf(b, "\tpkg := importer.RegisterPackage(%q, %q)\n\n", plan.Path, plan.GoName)
	emitFunctionRegistrations(b, plan.Symbols.Funcs, plan.Refs.PkgRef)
	emitConstantRegistrations(b, plan.Symbols.Consts, plan.Refs.PkgRef)
	emitVariableRegistrations(b, plan.Symbols.Vars, plan.Refs.PkgRef)
	emitTypeRegistrations(b, plan.Symbols.Types, plan.Refs.PkgRef)
	emitInterfaceProxyRegistrations(b, plan.Adapters.InterfaceProxies)
	emitMethodDirectCallRegistrations(b, plan.Adapters.MethodDirectCalls)
	b.WriteString("}\n\n")
}

func emitFunctionRegistrations(b *strings.Builder, funcs []*funcInfo, pkgRef string) {
	if len(funcs) == 0 {
		return
	}
	b.WriteString("\t// Functions\n")
	for _, fi := range funcs {
		directRef := "nil"
		if fi.DirectCall != "" {
			directRef = fmt.Sprintf("direct_%s_%s", pkgRef, fi.Name)
		}
		writeGeneratedf(b, "\tpkg.AddFunction(%q, %s.%s, \"\", %s)\n", fi.Name, pkgRef, fi.Name, directRef)
	}
	b.WriteString("\n")
}

func emitConstantRegistrations(b *strings.Builder, consts []*constInfo, pkgRef string) {
	if len(consts) == 0 {
		return
	}
	b.WriteString("\t// Constants\n")
	for _, ci := range consts {
		constRef := fmt.Sprintf("%s.%s", pkgRef, ci.Name)
		if needsUintCast(ci.Obj) {
			constRef = fmt.Sprintf("uint64(%s)", constRef)
		}
		writeGeneratedf(b, "\tpkg.AddConstant(%q, %s, \"\")\n", ci.Name, constRef)
	}
	b.WriteString("\n")
}

func emitVariableRegistrations(b *strings.Builder, vars []*varInfo, pkgRef string) {
	if len(vars) == 0 {
		return
	}
	b.WriteString("\t// Variables\n")
	for _, vi := range vars {
		writeGeneratedf(b, "\tpkg.AddVariable(%q, &%s.%s, \"\")\n", vi.Name, pkgRef, vi.Name)
	}
	b.WriteString("\n")
}

func emitTypeRegistrations(b *strings.Builder, typesToRegister []*typeInfo, pkgRef string) {
	if len(typesToRegister) == 0 {
		return
	}
	b.WriteString("\t// Types\n")
	for _, ti := range typesToRegister {
		expr := typeToReflectExpr(ti.Obj.Type(), pkgRef)
		if expr != "" {
			writeGeneratedf(b, "\tpkg.AddType(%q, %s, \"\")\n", ti.Name, expr)
		}
	}
	b.WriteString("\n")
}

func emitInterfaceProxyRegistrations(b *strings.Builder, proxies []*interfaceProxyInfo) {
	// RequiredMethods is stored as strings in generated code so the importer can
	// validate script-side method sets before allowing interface proxy creation.
	if len(proxies) == 0 {
		return
	}
	b.WriteString("\t// Interface Proxies\n")
	for _, proxy := range proxies {
		methods := make([]string, 0, len(proxy.RequiredMethods))
		for _, method := range proxy.RequiredMethods {
			methods = append(methods, fmt.Sprintf("%q", method))
		}
		writeGeneratedf(b, "\tpkg.AddInterfaceProxy(%q, %s, []string{%s}, %s)\n",
			proxy.TypeName, proxy.InterfaceTypeExpr, strings.Join(methods, ", "), proxy.FactoryName)
	}
	b.WriteString("\n")
}

func emitMethodDirectCallRegistrations(b *strings.Builder, methodDCs []*methodDirectCallInfo) {
	if len(methodDCs) == 0 {
		return
	}
	b.WriteString("\t// Method DirectCalls\n")
	for _, mdc := range methodDCs {
		writeGeneratedf(b, "\tpkg.AddMethodDirectCall(%q, %q, %s)\n",
			mdc.TypeName, mdc.MethodName, mdc.FuncName)
	}
	b.WriteString("\n")
}

func emitFunctionDirectCalls(b *strings.Builder, funcs []*funcInfo) {
	for _, fi := range funcs {
		if fi.DirectCall == "" {
			continue
		}
		b.WriteString(fi.DirectCall)
		b.WriteString("\n")
	}
}

func writeGeneratedf(b *strings.Builder, format string, args ...any) {
	// strings.Builder never returns a write error, so generated source emission
	// can centralize and intentionally discard fmt's writer error here.
	_, _ = fmt.Fprintf(b, format, args...)
}

func emitMethodDirectCalls(b *strings.Builder, methodDCs []*methodDirectCallInfo) {
	for _, mdc := range methodDCs {
		b.WriteString(mdc.Code)
		b.WriteString("\n")
	}
}

func emitInterfaceProxies(b *strings.Builder, proxies []*interfaceProxyInfo) {
	for _, proxy := range proxies {
		b.WriteString(proxy.Code)
		b.WriteString("\n")
	}
}
