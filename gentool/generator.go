// Package gentool provides the core dependency generation logic for gig.
// It generates package registration files with typed DirectCall wrappers.
package gentool

import (
	"fmt"
	"go/ast"
	"go/format"
	"go/importer"
	"go/parser"
	"go/token"
	"go/types"
	"os"
	"path/filepath"
	"strings"
)

// currentPkgPath is set per-package during generation to resolve same-package named types.
var currentPkgPath string

// ParsePkgsFile parses a Go source file and extracts all import paths.
func ParsePkgsFile(filePath string) ([]string, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, filePath, nil, parser.ImportsOnly)
	if err != nil {
		return nil, fmt.Errorf("parse %s: %w", filePath, err)
	}

	var paths []string
	for _, imp := range f.Imports {
		p := strings.Trim(imp.Path.Value, `"`)
		if p == "" {
			continue
		}
		paths = append(paths, p)
	}
	if len(paths) == 0 {
		return nil, fmt.Errorf("no imports found in %s", filePath)
	}
	return paths, nil
}

// GenerateBarrelImport creates the barrel stdlib.go that blank-imports all generated sub-packages.
func GenerateBarrelImport(outDir string, modulePath string, generatedPkgs []string) error {
	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	b.WriteString("// This file blank-imports all generated packages to trigger their init() registrations.\n")
	b.WriteString("package packages\n\n")
	b.WriteString("import (\n")
	for _, pkgName := range generatedPkgs {
		b.WriteString(fmt.Sprintf("\t_ %q\n", modulePath+"/packages/"+pkgName))
	}
	b.WriteString(")\n")

	code, err := format.Source([]byte(b.String()))
	if err != nil {
		code = []byte(b.String())
	}

	outFile := filepath.Join(outDir, "packages", "stdlib.go")
	return os.WriteFile(outFile, code, 0o666)
}

// PackageImport generates registration code for a single package.
// outDir is the output directory (e.g., "./mydep").
// pkgName is the package name for generated files (e.g., "mydep").
func PackageImport(path string, outDir string, pkgName string) error {
	pkg, err := importer.ForCompiler(token.NewFileSet(), "source", nil).Import(path)
	if err != nil {
		return err
	}

	currentPkgPath = path

	// Determine import alias for sub-packages.
	importAlias := ""
	if strings.Contains(path, "/") {
		importAlias = sanitizePkgName(path)
	}
	pkgRef := pkg.Name()
	if importAlias != "" {
		pkgRef = importAlias
	}

	var funcs []*funcInfo
	var consts []*constInfo
	var vars []*varInfo
	var typeNames []*typeInfo

	scope := pkg.Scope()
	for _, name := range scope.Names() {
		if !ast.IsExported(name) {
			continue
		}
		obj := scope.Lookup(name)

		switch o := obj.(type) {
		case *types.Func:
			sig := o.Type().(*types.Signature)
			if sig.TypeParams().Len() > 0 {
				continue
			}
			fi := &funcInfo{Name: name, Sig: sig}
			fi.DirectCall = generateDirectCall(fi, pkgRef)
			funcs = append(funcs, fi)

		case *types.Const:
			consts = append(consts, &constInfo{Name: name, Obj: o})

		case *types.Var:
			vars = append(vars, &varInfo{Name: name, Obj: o})

		case *types.TypeName:
			if o.IsAlias() {
				continue
			}
			if named, ok := o.Type().(*types.Named); ok {
				if named.TypeParams().Len() > 0 {
					continue
				}
			}
			if iface, ok := o.Type().Underlying().(*types.Interface); ok {
				if !iface.IsMethodSet() {
					continue
				}
			}
			typeNames = append(typeNames, &typeInfo{Name: name, Obj: o})
		}
	}

	if len(funcs) == 0 && len(consts) == 0 && len(vars) == 0 && len(typeNames) == 0 {
		fmt.Printf("  (skipped, nothing to register)\n")
		return nil
	}

	goPkgName := sanitizePkgName(path)
	needReflect := false
	for _, ti := range typeNames {
		if typeToReflectExpr(ti.Obj.Type(), pkgRef) != "" {
			needReflect = true
			break
		}
	}
	hasDirectCalls := false
	for _, fi := range funcs {
		if fi.DirectCall != "" {
			hasDirectCalls = true
			break
		}
	}

	b := &strings.Builder{}
	b.WriteString("// Code generated by gig/gentool. DO NOT EDIT.\n")
	b.WriteString(fmt.Sprintf("package %s\n\n", pkgName))

	b.WriteString("import (\n")
	if importAlias != "" {
		b.WriteString(fmt.Sprintf("\t%s %q\n", importAlias, path))
	} else {
		b.WriteString(fmt.Sprintf("\t%q\n", path))
	}
	if needReflect {
		b.WriteString("\t\"reflect\"\n")
	}
	b.WriteString("\n")
	b.WriteString("\t\"gig/importer\"\n")
	if hasDirectCalls {
		b.WriteString("\t\"gig/value\"\n")
	}
	b.WriteString(")\n\n")

	b.WriteString("func init() {\n")
	b.WriteString(fmt.Sprintf("\tpkg := importer.RegisterPackage(%q, %q)\n\n", path, pkg.Name()))

	if len(funcs) > 0 {
		b.WriteString("\t// Functions\n")
		for _, fi := range funcs {
			directRef := "nil"
			if fi.DirectCall != "" {
				directRef = fmt.Sprintf("direct_%s_%s", pkgRef, fi.Name)
			}
			b.WriteString(fmt.Sprintf("\tpkg.AddFunction(%q, %s.%s, \"\", %s)\n",
				fi.Name, pkgRef, fi.Name, directRef))
		}
		b.WriteString("\n")
	}

	if len(consts) > 0 {
		b.WriteString("\t// Constants\n")
		for _, ci := range consts {
			constRef := fmt.Sprintf("%s.%s", pkgRef, ci.Name)
			if ci.Name == "MaxUint64" || ci.Name == "MaxUint" {
				constRef = fmt.Sprintf("uint(%s)", constRef)
			}
			b.WriteString(fmt.Sprintf("\tpkg.AddConstant(%q, %s, \"\")\n", ci.Name, constRef))
		}
		b.WriteString("\n")
	}

	if len(vars) > 0 {
		b.WriteString("\t// Variables\n")
		for _, vi := range vars {
			b.WriteString(fmt.Sprintf("\tpkg.AddVariable(%q, &%s.%s, \"\")\n", vi.Name, pkgRef, vi.Name))
		}
		b.WriteString("\n")
	}

	if len(typeNames) > 0 {
		b.WriteString("\t// Types\n")
		for _, ti := range typeNames {
			expr := typeToReflectExpr(ti.Obj.Type(), pkgRef)
			if expr != "" {
				b.WriteString(fmt.Sprintf("\tpkg.AddType(%q, %s, \"\")\n", ti.Name, expr))
			}
		}
		b.WriteString("\n")
	}

	b.WriteString("}\n\n")

	for _, fi := range funcs {
		if fi.DirectCall != "" {
			b.WriteString(fi.DirectCall)
			b.WriteString("\n")
		}
	}

	src := b.String()
	code, err := format.Source([]byte(src))
	if err != nil {
		code = []byte(src)
		fmt.Printf("  format error for %s: %s\n", path, err.Error())
	}

	// Output file: outDir/goPkgName.go (e.g., mydep/fmt.go)
	filename := filepath.Join(outDir, goPkgName+".go")
	fmt.Printf("  -> %s\n", filename)
	return os.WriteFile(filename, code, 0o666)
}

// --- Type info structs ---

type funcInfo struct {
	Name       string
	Sig        *types.Signature
	DirectCall string
}

type constInfo struct {
	Name string
	Obj  *types.Const
}

type varInfo struct {
	Name string
	Obj  *types.Var
}

type typeInfo struct {
	Name string
	Obj  *types.TypeName
}

// --- DirectCall code generation ---

func generateDirectCall(fi *funcInfo, pkgRef string) string {
	sig := fi.Sig
	params := sig.Params()
	results := sig.Results()

	if sig.Recv() != nil {
		return ""
	}

	isVariadic := sig.Variadic()
	fixedCount := params.Len()
	if isVariadic {
		fixedCount--
	}

	for i := 0; i < fixedCount; i++ {
		if !canWrapParam(params.At(i).Type(), pkgRef) {
			return ""
		}
	}
	if isVariadic {
		sliceType := params.At(params.Len() - 1).Type().(*types.Slice)
		elemType := sliceType.Elem()
		if !canWrapParam(elemType, pkgRef) && !isEmptyInterface(elemType) {
			return ""
		}
	}
	if results.Len() > 3 {
		return ""
	}

	b := &strings.Builder{}
	b.WriteString(fmt.Sprintf("func direct_%s_%s(args []value.Value) value.Value {\n", pkgRef, fi.Name))

	var argExprs []string
	for i := 0; i < fixedCount; i++ {
		paramType := params.At(i).Type()
		expr := extractArg(paramType, fmt.Sprintf("args[%d]", i), pkgRef)
		if expr == "" {
			return ""
		}
		argName := fmt.Sprintf("a%d", i)
		b.WriteString(fmt.Sprintf("\t%s := %s\n", argName, expr))
		argExprs = append(argExprs, argName)
	}

	if isVariadic {
		sliceType := params.At(params.Len() - 1).Type().(*types.Slice)
		elemType := sliceType.Elem()

		if isEmptyInterface(elemType) {
			b.WriteString(fmt.Sprintf("\tvarArgs := make([]interface{}, len(args)-%d)\n", fixedCount))
			b.WriteString(fmt.Sprintf("\tfor i := %d; i < len(args); i++ {\n", fixedCount))
			b.WriteString(fmt.Sprintf("\t\tvarArgs[i-%d] = args[i].Interface()\n", fixedCount))
			b.WriteString("\t}\n")
			argExprs = append(argExprs, "varArgs...")
		} else {
			elemTypeStr := resolveTypeName(elemType, pkgRef)
			elemExtract := extractArg(elemType, "args[i]", pkgRef)
			if elemTypeStr == "" || elemExtract == "" {
				return ""
			}
			b.WriteString(fmt.Sprintf("\tvarArgs := make([]%s, len(args)-%d)\n", elemTypeStr, fixedCount))
			b.WriteString(fmt.Sprintf("\tfor i := %d; i < len(args); i++ {\n", fixedCount))
			b.WriteString(fmt.Sprintf("\t\tvarArgs[i-%d] = %s\n", fixedCount, elemExtract))
			b.WriteString("\t}\n")
			argExprs = append(argExprs, "varArgs...")
		}
	}

	callExpr := fmt.Sprintf("%s.%s(%s)", pkgRef, fi.Name, strings.Join(argExprs, ", "))

	switch results.Len() {
	case 0:
		b.WriteString(fmt.Sprintf("\t%s\n", callExpr))
		b.WriteString("\treturn value.MakeNil()\n")
	case 1:
		retExpr := wrapReturn(results.At(0).Type(), callExpr)
		if retExpr == "" {
			return ""
		}
		b.WriteString(fmt.Sprintf("\treturn %s\n", retExpr))
	case 2:
		b.WriteString(fmt.Sprintf("\tr0, r1 := %s\n", callExpr))
		b.WriteString(fmt.Sprintf("\treturn value.FromInterface([]interface{}{%s, %s})\n",
			"r0", "r1"))
	case 3:
		b.WriteString(fmt.Sprintf("\tr0, r1, r2 := %s\n", callExpr))
		b.WriteString(fmt.Sprintf("\treturn value.FromInterface([]interface{}{%s, %s, %s})\n",
			"r0", "r1", "r2"))
	default:
		return ""
	}

	b.WriteString("}\n")
	return b.String()
}

// --- Param analysis ---

func canWrapParam(t types.Type, pkgRef string) bool {
	if named, ok := t.(*types.Named); ok {
		obj := named.Obj()
		pkg := obj.Pkg()

		if pkg == nil {
			return obj.Name() == "error"
		}

		if pkg.Path() != currentPkgPath {
			return false
		}

		return canWrapUnderlying(t.Underlying())
	}

	return canWrapUnderlying(t.Underlying())
}

func canWrapUnderlying(t types.Type) bool {
	switch ut := t.(type) {
	case *types.Basic:
		return ut.Kind() != types.UnsafePointer && ut.Kind() != types.Invalid
	case *types.Slice:
		_, ok := ut.Elem().Underlying().(*types.Basic)
		return ok
	case *types.Interface:
		return ut.NumMethods() == 0
	default:
		return false
	}
}

// --- Argument extraction ---

func extractArg(t types.Type, valExpr string, pkgRef string) string {
	if named, ok := t.(*types.Named); ok {
		obj := named.Obj()
		if obj.Pkg() == nil && obj.Name() == "error" {
			return fmt.Sprintf("%s.Interface().(error)", valExpr)
		}
	}

	if named, ok := t.(*types.Named); ok {
		underlying := t.Underlying()
		if bt, ok := underlying.(*types.Basic); ok {
			basicExpr := extractBasic(bt, valExpr)
			if basicExpr == "" {
				return ""
			}
			namedName := resolveTypeName(named, pkgRef)
			if namedName == "" {
				return ""
			}
			return fmt.Sprintf("%s(%s)", namedName, basicExpr)
		}
		return extractUnderlying(underlying, valExpr)
	}

	return extractUnderlying(t.Underlying(), valExpr)
}

func extractUnderlying(t types.Type, valExpr string) string {
	switch ut := t.(type) {
	case *types.Basic:
		return extractBasic(ut, valExpr)
	case *types.Slice:
		return extractSlice(ut, valExpr)
	case *types.Interface:
		return fmt.Sprintf("%s.Interface()", valExpr)
	default:
		return ""
	}
}

func extractBasic(bt *types.Basic, valExpr string) string {
	info := bt.Info()
	kind := bt.Kind()

	switch {
	case kind == types.Bool:
		return fmt.Sprintf("%s.Bool()", valExpr)
	case info&types.IsInteger != 0 && info&types.IsUnsigned != 0:
		switch kind {
		case types.Uint8:
			return fmt.Sprintf("byte(%s.Uint())", valExpr)
		case types.Uint16:
			return fmt.Sprintf("uint16(%s.Uint())", valExpr)
		case types.Uint32:
			return fmt.Sprintf("uint32(%s.Uint())", valExpr)
		case types.Uint64:
			return fmt.Sprintf("%s.Uint()", valExpr)
		case types.Uint:
			return fmt.Sprintf("uint(%s.Uint())", valExpr)
		case types.Uintptr:
			return fmt.Sprintf("uintptr(%s.Uint())", valExpr)
		default:
			return fmt.Sprintf("uint(%s.Uint())", valExpr)
		}
	case info&types.IsInteger != 0:
		switch kind {
		case types.Int8:
			return fmt.Sprintf("int8(%s.Int())", valExpr)
		case types.Int16:
			return fmt.Sprintf("int16(%s.Int())", valExpr)
		case types.Int32:
			return fmt.Sprintf("int32(%s.Int())", valExpr)
		case types.Int64:
			return fmt.Sprintf("%s.Int()", valExpr)
		case types.Int:
			return fmt.Sprintf("int(%s.Int())", valExpr)
		default:
			return fmt.Sprintf("int(%s.Int())", valExpr)
		}
	case info&types.IsFloat != 0:
		switch kind {
		case types.Float32:
			return fmt.Sprintf("float32(%s.Float())", valExpr)
		case types.Float64:
			return fmt.Sprintf("%s.Float()", valExpr)
		default:
			return fmt.Sprintf("%s.Float()", valExpr)
		}
	case info&types.IsComplex != 0:
		return fmt.Sprintf("%s.Complex()", valExpr)
	case info&types.IsString != 0:
		return fmt.Sprintf("%s.String()", valExpr)
	case kind == types.UnsafePointer:
		return ""
	default:
		return fmt.Sprintf("%s.Interface()", valExpr)
	}
}

func extractSlice(st *types.Slice, valExpr string) string {
	if bt, ok := st.Elem().Underlying().(*types.Basic); ok {
		switch bt.Kind() {
		case types.Byte:
			return fmt.Sprintf("%s.Interface().([]byte)", valExpr)
		case types.String:
			return fmt.Sprintf("%s.Interface().([]string)", valExpr)
		case types.Int:
			return fmt.Sprintf("%s.Interface().([]int)", valExpr)
		case types.Int32:
			return fmt.Sprintf("%s.Interface().([]int32)", valExpr)
		case types.Int64:
			return fmt.Sprintf("%s.Interface().([]int64)", valExpr)
		case types.Uint16:
			return fmt.Sprintf("%s.Interface().([]uint16)", valExpr)
		case types.Uint32:
			return fmt.Sprintf("%s.Interface().([]uint32)", valExpr)
		case types.Uint64:
			return fmt.Sprintf("%s.Interface().([]uint64)", valExpr)
		case types.Float64:
			return fmt.Sprintf("%s.Interface().([]float64)", valExpr)
		case types.Float32:
			return fmt.Sprintf("%s.Interface().([]float32)", valExpr)
		default:
			typeName := bt.Name()
			return fmt.Sprintf("%s.Interface().([]%s)", valExpr, typeName)
		}
	}
	return fmt.Sprintf("%s.Interface()", valExpr)
}

// --- Return value wrapping ---

func wrapReturn(t types.Type, goExpr string) string {
	if bt, ok := t.Underlying().(*types.Basic); ok {
		return wrapBasicReturn(bt, goExpr)
	}
	return fmt.Sprintf("value.FromInterface(%s)", goExpr)
}

func wrapBasicReturn(bt *types.Basic, goExpr string) string {
	info := bt.Info()
	kind := bt.Kind()

	switch {
	case kind == types.Bool:
		return fmt.Sprintf("value.MakeBool(%s)", goExpr)
	case info&types.IsInteger != 0 && info&types.IsUnsigned != 0:
		return fmt.Sprintf("value.MakeUint(uint64(%s))", goExpr)
	case info&types.IsInteger != 0:
		return fmt.Sprintf("value.MakeInt(int64(%s))", goExpr)
	case info&types.IsFloat != 0:
		return fmt.Sprintf("value.MakeFloat(float64(%s))", goExpr)
	case info&types.IsComplex != 0:
		return fmt.Sprintf("value.FromInterface(%s)", goExpr)
	case info&types.IsString != 0:
		return fmt.Sprintf("value.MakeString(string(%s))", goExpr)
	default:
		return fmt.Sprintf("value.FromInterface(%s)", goExpr)
	}
}

// --- Type name resolution ---

func resolveTypeName(t types.Type, pkgRef string) string {
	switch tt := t.(type) {
	case *types.Named:
		obj := tt.Obj()
		pkg := obj.Pkg()
		if pkg == nil {
			return obj.Name()
		}
		if pkg.Path() == currentPkgPath {
			return fmt.Sprintf("%s.%s", pkgRef, obj.Name())
		}
		return ""
	case *types.Basic:
		return tt.Name()
	case *types.Pointer:
		elem := resolveTypeName(tt.Elem(), pkgRef)
		if elem == "" {
			return ""
		}
		return "*" + elem
	case *types.Slice:
		elem := resolveTypeName(tt.Elem(), pkgRef)
		if elem == "" {
			return ""
		}
		return "[]" + elem
	case *types.Interface:
		if tt.NumMethods() == 0 {
			return "interface{}"
		}
		return ""
	default:
		return ""
	}
}

// --- Utility ---

func isEmptyInterface(t types.Type) bool {
	iface, ok := t.Underlying().(*types.Interface)
	return ok && iface.NumMethods() == 0
}

func typeToReflectExpr(t types.Type, pkgRef string) string {
	named, ok := t.(*types.Named)
	if !ok {
		return ""
	}
	name := named.Obj().Name()

	switch t.Underlying().(type) {
	case *types.Interface:
		return fmt.Sprintf("reflect.TypeOf((*%s.%s)(nil)).Elem()", pkgRef, name)
	case *types.Struct:
		return fmt.Sprintf("reflect.TypeOf(%s.%s{})", pkgRef, name)
	default:
		return fmt.Sprintf("reflect.TypeOf((*%s.%s)(nil)).Elem()", pkgRef, name)
	}
}

func sanitizePkgName(path string) string {
	return strings.NewReplacer(
		"/", "_",
		"-", "_",
		".", "_",
	).Replace(path)
}
