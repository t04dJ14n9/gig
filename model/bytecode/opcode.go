	// Operands: [count:2]
	// Stack: [... values...] -> [... []value.Value]
	OpPack

	// OpUnpack pops a tuple/slice value and pushes each element back to the stack.
	// Stack: [... tuple] -> [... elements...]
	OpUnpack

	// OpHalt aborts execution with a "halt" error for debugging.
	OpHalt

